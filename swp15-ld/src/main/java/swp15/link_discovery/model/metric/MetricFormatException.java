package swp15.link_discovery.model.metric;

public class MetricFormatException extends RuntimeException
{
	private static final long	serialVersionUID	= -1394278085750921482L;
	public MetricFormatException() {super();}
	public MetricFormatException(String s) {super(s);}
	public MetricFormatException(String s,Throwable cause) {super(s,cause);}
}